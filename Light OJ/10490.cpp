ll sieve_size;
bitset <10000010> bs;
vi primes;

void sieve(ll upperbound)
{
    sieve_size=upperbound+1;
    bs.set();
    bs[0]=bs[1]=0;
    for(ll i=2;i<=sieve_size;i++)
    {
        if(bs[i])
        {
            for(ll j=i*i;j<=sieve_size;j+=i)
                bs[j]=0;
            primes.PB(i);
        }
    }
}

bool isPrime(ll n)
{
    if(n<=sieve_size) return bs[n];
    for(int i=0;i<primes.size();i++)
    {
        if(n%primes[i]==0) return false;
    }
    return true;
}

