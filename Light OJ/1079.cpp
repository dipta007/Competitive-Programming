double call(int in, int dollar)
{
    if(dollar<=0) return 0;
    if(in>=n)
    {
        if(dollar<=0)
        {
            return 0;
        }
        return 10000;
    }
    if(vis[in][dollar]==1) return dp[in][dollar];
    vis[in][dollar]=1;

    double p1 = call(in+1, dollar);
    double p2 = p[in] + (1.0-p[in])*call(in+1, dollar-cost[in]);
    return dp[in][dollar] = min(p1,p2);
}

