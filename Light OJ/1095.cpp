int n,m,k;
ll mod=1000000007;
ll dp[1004][1004];
ll dp1[1004];

ll nCr(ll n,ll r)
{
    if(r==1)
        return n;
    if(n==r || r==0)
        return 1;
    ll &ret=dp[n][r];
    if(ret!=-1)
        return ret;
    ret=(nCr(n-1,r-1)%mod+nCr(n-1,r)%mod)%mod;
    return ret;
}

ll call(int in)
{
    if(in==1)
    {
        return 0;
    }
    if(in==0)
    {
        return 1;
    }

    ll &ret = dp1[in];
    if(ret!=-1) return ret;

    ret = 0;

    ret = ((ll)(in-1) * (call(in-1) + call(in-2)))%mod;

    return ret;
}

