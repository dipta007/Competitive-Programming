#define M 100000004
bool marked[M];
int res[M];

bool isPrime(int n)
{
    if (n < 2) return 1;
    if (n == 2) return 0;
    if (n % 2 == 0) return 1;
    return marked[n];
}

void sieve(int n)
{
    marked[0]=marked[1]=1;
    for (int i = 3; i * i <= n; i += 2)
    {
        if (marked[i] == 0)
        {
            for (int j = i * i; j <= n; j += i + i)
            {
                marked[j] = 1;
            }
        }
    }
    res[0]=0;
    for(int i=1; i<=M-4; i++)
    {
        res[i]=res[i-1]+(int)!isPrime(i);
    }
}



