bool checkProgram(string st)
{
    if(st.size()<7) return 0;
    string kk = "";
    for(int i=st.size()-1,k=1;k<=9;k++,i--)
    {
        kk += st[i];
    }
    reverse(ALL(kk));
    if(kk=="::PROGRAM") return 1;
    return 0;
}

