inline double sqr(double x) {
    return x*x;
}

struct point {
    double x,y;
    point() {}
    point(double _x,double _y):
        x(_x),y(_y) {};

    double distance(point p) {
        return hypot(x-p.x,y-p.y);
    }
};

struct vector2d {
    double x,y;
    vector2d(point a, point b)
    {
        x = b.x - a.x;
        y = b.y - a.y;
    }
    vector2d(double a, double b)
    {
        x = a, y = b;
    }
};

double angle(double a, double b, double c)
{
    return acos( (sqr(a) + sqr(b) - sqr(c)) / (2.0 * a * b) );
}
double angle(vector2d a, vector2d b)
{
    double upor = a.x * b.x + a.y * b.y;
    double aa = sqrt(sqr(a.x) + sqr(a.y));
    double bb = sqrt(sqr(b.x) + sqr(b.y));

    return acos((upor / aa )/ bb);
}

