#define M 1000000
bool marked[M];

bool isPrime(int n) {
  if (n < 2) return 1;
  if (n == 2) return 0;
  if (n % 2 == 0) return 1;
  return marked[n];
}

void sieve(int n) {
  for (int i = 3; i * i <= n; i += 2) {
    if (marked[i] == 0) {
      for (int j = i * i; j <= n; j += i + i) {
        marked[j] = 1;
      }
    }
  }
}


