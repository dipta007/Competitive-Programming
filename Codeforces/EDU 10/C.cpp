vector < vector<int> > AdjList;
vi v;
map <int,int> mp;
map <pii,bool> mark;

int main() {
    READ("in.txt");
    //WRITE("out.txt");

    ll n,m;
    while(~getLL(n,m))
    {
        v.clear();
        mp.clear();
        mark.clear();
        for(ll i=0;i<n;i++)
        {
            int x;
            getI(x);
            v.PB(x);
            mp[x]=i;
        }
        AdjList.assign(n+4,vi());
        for(ll i=0;i<m;i++)
        {
            int x,y;
            getII(x,y);
            if(mp[x]>mp[y]) swap(x,y);
            if(mark[pii(x,y)]==0)
            {
                //debug(x,y)
                mark[pii(x,y)]=1;
                AdjList[x].PB(y);
                //AdjList[y].PB(x);
            }
        }
        ll low=0;

        ll res = (n*(n+1))/2;
        for(ll i=0;i<n;i++)
        {
            int val = v[i];
            for(int j=0;j<AdjList[val].size();j++)
            {
                ll in = mp[ AdjList[val][j] ];
                if(in<i) continue;

                ll left = i-low+1;
                low = i+1;
                ll right = n-in;

                res -= (left*right);
            }
        }
        printf("%I64d\n",res);
    }


    return 0;
}


