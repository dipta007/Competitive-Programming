#define MAX 20000010

using namespace std;

int n;
bool placed[200010];
char dp[MAX], str[MAX], sum[MAX];

void print(int x){
    assert(dp[x] <= 2);

    char str[15];
    int i, j, l, s, d;

    for (i = 0; i < 2; i++){
        sprintf(str, "%d", x);
        l = strlen(str);
        for (j = 0, x = 0; j < l; j++){
            putchar(str[j]);
            putchar((j + 1) == l ? 10 : '+');
            x += (str[j] - 48);
        }
    }
}

int main(){
    int i, j, k, l, s, x, iterations;

    for (i = 0; i < MAX; i++){
        if (i < 10){
            dp[i] = 0;
            sum[i] = i;
        }
        else{
            sum[i] = sum[i / 10] + (i % 10);
            dp[i] = dp[sum[i]] + 1;
        }
    }

    while (scanf("%d", &n) != EOF){
        scanf("%s", str);

        iterations = 0;
        while (1){
            CLR(placed);
            i = 0, s = 0, iterations++;

            while (i < n){
                x = str[i] - 48;
                if ((rand() & 1) && (i + 1) < n){
                    i++;
                    placed[i] = true;
                    x = (x * 10) + str[i] - 48;
                }
                else placed[i] = true;
                s += x, i++;
            }

            assert(s < MAX);
            if (dp[s] <= 2){
                for (i = 0; i < n; i++){
                    putchar(str[i]);
                    if (placed[i] && (i + 1) < n) putchar('+');
                }
                puts("");
                print(s);
                break;
            }
        }

        fprintf(stderr, "Iterations = %d\n", iterations);
    }
    return 0;
}
