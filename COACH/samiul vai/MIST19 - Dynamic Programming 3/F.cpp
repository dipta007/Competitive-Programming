int call(int in, int diff,int flg)
{
    int dxx=abs(diff);
    //debug(dxx)
    if(in>=n)
    {
        if(diff==0) return 0;
        return -INF;
    }
    int &ret = dp[flg][dxx];
    if(vis[flg][dxx]==cs) return ret;

    int res=-INF;
//    if((diff+a[in])<=sum)
        res = max(res, a[in] + call(in+1, diff+a[in], flg^1) );
    if(abs(diff-a[in])<=sum)
        res = max(res, call(in+1, diff-a[in], flg^1));
    res = max(res, call(in+1, diff, flg^1));

    //debug(res,a[in],in)
    vis[flg][dxx]=cs;

    return ret = res;
}

